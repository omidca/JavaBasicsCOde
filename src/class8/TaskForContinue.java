package class8;

public class TaskForContinue {

	public static void main(String[] args) {
		
		
		// Print numbers from 1 to 50 except those that are divisible by3.
		
		
		for (int i=1; i<=50; i++) {
			if(i==3 || i==6 || i==9 || i==12 || i==15 || i==18 || i==21 || i==24 || i==27 || i==27 || i==30 || i==33 || i==36 || i==39 || i==42 || i==45 || i==48) {
				continue;
			}
			System.out.print(i+" ");
		}
		
		
		
		
	}
	}

