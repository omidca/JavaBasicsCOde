package class8;

public class ForLoopIntro {

	public static void main(String[] args) {
	
		//I want to say good morning 5 times

		for(int i=1; i<=5; i++) {
			System.out.println("Good Morning ");
		}
		
		System.out.println("====================================");
		// another example 
		for (int a=1; a<8; a++) {
			System.out.println("I love you ");
		}
		System.out.println("====================================");
		
		
	}

}
